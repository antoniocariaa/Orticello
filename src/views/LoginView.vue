<script setup>
import { ref, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import { useI18n } from 'vue-i18n'
import api from '../services/api'
import { store } from '../store'

const router = useRouter()
const { t } = useI18n()

const form = ref({
  email: '',
  password: ''
})

const showPassword = ref(false)
const loading = ref(false)
const loadingGoogle = ref(false)
const error = ref(null)

const GOOGLE_CLIENT_ID = import.meta.env.VITE_GOOGLE_CLIENT_ID

const loginWithGoogle = () => {
  if (!GOOGLE_CLIENT_ID) {
    error.value = t('auth.google_client_id_missing')
    return
  }
  
  if (!window.google) {
    error.value = t('auth.google_signin_not_loaded')
    return
  }
  
  try {
    // Forza la visualizzazione del prompt anche se soppresso
    window.google.accounts.id.prompt()
  } catch (err) {
    console.error('Errore Google Sign-In:', err)
    error.value = t('auth.google_failed')
  }
}

const login = async () => {
  loading.value = true
  error.value = null
  try {
    const data = await api.post('/authentication', form.value)

    // Save token
    localStorage.setItem('token', data.token)
    
    // Fetch full user details
    const userId = data.id 
    
    const user = await api.get(`/utenti/${userId}`)
    
    localStorage.setItem('user', JSON.stringify(user))
    store.setUser(user)

    // Redirect to home or dashboard
    router.push('/')
  } catch (err) {
    error.value = err.message
    // Clear token if login fails
    if (localStorage.getItem('token') && !store.isAuthenticated) {
        localStorage.removeItem('token')
    }
  } finally {
    loading.value = false
  }
}

const handleGoogleLogin = async (response) => {
  loadingGoogle.value = true
  error.value = null
  
  try {
    const data = await api.post('/authentication', {
      googleToken: response.credential
    })

    // Save token
    localStorage.setItem('token', data.token)
    
    // Fetch full user details
    const userId = data.id 
    
    const user = await api.get(`/utenti/${userId}`)
    
    localStorage.setItem('user', JSON.stringify(user))
    store.setUser(user)

    // Redirect to home or dashboard
    router.push('/')
  } catch (err) {
    // Se l'utente non esiste (401), estrai i dati da Google e vai alla registrazione
    if (err.status === 401) {
      try {
        // Decodifica il JWT di Google per estrarre i dati utente
        const base64Url = response.credential.split('.')[1]
        const base64 = base64Url.replace(/-/g, '+').replace(/_/g, '/')
        const jsonPayload = decodeURIComponent(atob(base64).split('').map(c => {
          return '%' + ('00' + c.charCodeAt(0).toString(16)).slice(-2)
        }).join(''))
        
        const googleUser = JSON.parse(jsonPayload)
        
        // Reindirizza alla registrazione con i dati di Google
        router.push({
          path: '/register',
          state: {
            fromGoogle: true,
            googleToken: response.credential,
            nome: googleUser.given_name || '',
            cognome: googleUser.family_name || '',
            email: googleUser.email || ''
          }
        })
        return
      } catch (decodeErr) {
        console.error('Errore decodifica token Google:', decodeErr)
      }
    }
    
    error.value = err.message || 'Errore durante l\'accesso con Google'
    // Clear token if login fails
    if (localStorage.getItem('token') && !store.isAuthenticated) {
        localStorage.removeItem('token')
    }
  } finally {
    loadingGoogle.value = false
  }
}

onMounted(() => {
  // Wait for Google script to load
  const initGoogleSignIn = () => {
    if (window.google && GOOGLE_CLIENT_ID) {
      try {
        window.google.accounts.id.initialize({
          client_id: GOOGLE_CLIENT_ID,
          callback: handleGoogleLogin,
          auto_select: false,
          cancel_on_tap_outside: true
        })
        console.log('Google Sign-In inizializzato correttamente')
      } catch (err) {
        console.error('Errore inizializzazione Google Sign-In:', err)
      }
    } else {
      console.warn('Google Sign-In non disponibile:', { 
        googleLoaded: !!window.google, 
        clientIdSet: !!GOOGLE_CLIENT_ID 
      })
    }
  }

  // Try to initialize immediately, or wait for script to load
  if (window.google) {
    initGoogleSignIn()
  } else {
    // Wait for script to load
    const checkGoogle = setInterval(() => {
      if (window.google) {
        clearInterval(checkGoogle)
        initGoogleSignIn()
      }
    }, 100)
    
    // Timeout after 5 seconds
    setTimeout(() => clearInterval(checkGoogle), 5000)
  }
})
</script>

<template>
  <div class="w-full max-w-md mx-auto">
    <div class="flex flex-col items-center mb-6">
      <img src="/favicon.png" alt="Orticello Logo" class="w-20 h-20 mb-1 drop-shadow-lg" />
      <h1 class="text-3xl font-bold text-primary drop-shadow-md">Orticello</h1>
    </div>
    <div class="card bg-base-100 shadow-xl w-full">
      <div class="card-body p-6 sm:p-8">
      <h2 class="card-title text-2xl font-bold text-center justify-center mb-6 text-primary">{{ $t('auth.welcome_back') }}</h2>
      
      <form @submit.prevent="login" class="space-y-4">
        <div class="form-control">
          <label class="label">
            <span class="label-text">{{ $t('auth.email') }}</span>
          </label>
          <input type="email" v-model="form.email" placeholder="email@esempio.com" class="input input-bordered w-full" required />
        </div>

        <div class="form-control">
          <label class="label">
            <span class="label-text">{{ $t('auth.password') }}</span>
          </label>
          <div class="relative">
            <input 
              :type="showPassword ? 'text' : 'password'" 
              v-model="form.password" 
              placeholder="********" 
              class="input input-bordered w-full pr-10" 
              required 
            />
            <button 
              type="button" 
              @click="showPassword = !showPassword"
              class="absolute inset-y-0 right-0 flex items-center pr-3 text-gray-500 hover:text-base-content"
            >
              <svg v-if="!showPassword" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-5 h-5">
                <path stroke-linecap="round" stroke-linejoin="round" d="M2.036 12.322a1.012 1.012 0 010-.639C3.423 7.51 7.36 4.5 12 4.5c4.638 0 8.573 3.007 9.963 7.178.07.207.07.431 0 .639C20.577 16.49 16.64 19.5 12 19.5c-4.638 0-8.573-3.007-9.963-7.178z" />
                <path stroke-linecap="round" stroke-linejoin="round" d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
              </svg>
              <svg v-else xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-5 h-5">
                <path stroke-linecap="round" stroke-linejoin="round" d="M3.98 8.223A10.477 10.477 0 001.934 12C3.226 16.338 7.244 19.5 12 19.5c.993 0 1.953-.138 2.863-.395M6.228 6.228A10.45 10.45 0 0112 4.5c4.756 0 8.773 3.162 10.065 7.498a10.523 10.523 0 01-4.293 5.774M6.228 6.228L3 3m3.228 3.228l3.65 3.65m7.894 7.894L21 21m-3.228-3.228l-3.65-3.65m0 0a3 3 0 10-4.243-4.243m4.242 4.242L9.88 9.88" />
              </svg>
            </button>
          </div>
          <label class="label">
            <a href="#" class="label-text-alt link link-hover">{{ $t('auth.forgot_password') }}</a>
          </label>
        </div>

        <div v-if="error" role="alert" class="alert alert-error text-sm mt-4">
          <svg xmlns="http://www.w3.org/2000/svg" class="stroke-current shrink-0 h-6 w-6" fill="none" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M10 14l2-2m0 0l2-2m-2 2l-2-2m2 2l2 2m7-2a9 9 0 11-18 0 9 9 0 0118 0z" /></svg>
          <span>{{ error }}</span>
        </div>

        <div class="form-control mt-6">
          <div class="flex gap-2">
            <button type="submit" class="btn btn-primary flex-1" :disabled="loading || loadingGoogle">
              <span v-if="loading" class="loading loading-spinner"></span>
              {{ $t('auth.login') }}
            </button>
            <button 
              type="button" 
              @click="loginWithGoogle" 
              class="btn btn-outline flex-1" 
              :disabled="loading || loadingGoogle || !GOOGLE_CLIENT_ID"
            >
              <span v-if="loadingGoogle" class="loading loading-spinner"></span>
              <svg v-else xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" viewBox="0 0 24 24">
                <path fill="currentColor" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                <path fill="currentColor" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                <path fill="currentColor" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                <path fill="currentColor" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
              </svg>
              Google
            </button>
          </div>
        </div>
      </form>

      <div class="text-center mt-4">
        <p class="text-sm">{{ $t('auth.no_account') }} <router-link to="/register" class="link link-primary">{{ $t('auth.register') }}</router-link></p>
      </div>
      </div>
    </div>
  </div>
</template>